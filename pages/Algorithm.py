st.markdown(r"""
このページでは，本アプリで用いている行列生成アルゴリズムを簡単に説明します。

## 1. 目的

指定した Jordan 標準形 \(J\) に対して，相似変換

$$
A = QJQ^{-1}
$$

を用いることで，\(J\) と相似な整数行列 \(A\) を生成します。
生成された行列 \(A\) は，Jordan 標準形を求める演習問題として利用することを想定しています。

## 2. 基本的な手順

1. Jordan 標準形 \(J\) を入力する。
2. 基本行列をランダムに選び，相似変換を一定回数行う。
3. 得られた行列 \(A\) に対して評価値 \(F_{\mathrm{total}}(A)\) を計算する。
4. 評価値が小さくなる基本変換を探索する。
5. 評価値がこれ以上小さくならなければ，その行列を出力する。

## 3. 評価値

本アプリでは，行列 \(A=(a_{ij})\) に対して，次の評価値を用います。
評価値は「問題としての扱いにくさ」を表し，小さいほど良い行列とみなします。

### 成分の大きさ

$$
F_{\mathrm{abs}}(A)
=
\sum_{i,j} |a_{ij}|
$$

### 10を超える成分へのペナルティ

$$
F_{\mathrm{excess}}(A)
=
3\sum_{i,j}\max(|a_{ij}|-10,0)
$$

### 非ゼロ成分が1つだけの行・列へのペナルティ

非ゼロ成分が1つだけの行または列の個数を \(N_{\mathrm{sparse}}\)，
行列サイズを \(n\) とすると，

$$
F_{\mathrm{sparse}}(A)
=
10nN_{\mathrm{sparse}}
$$

### 0成分の個数に関するペナルティ

目標とする0成分の個数を \(B\)，実際の0成分の個数を \(Z(A)\) とします。

「ちょうど \(B\) 個」を目指す場合は，

$$
F_{\mathrm{zero}}(A)
=
5|B-Z(A)|^3
$$

とします。

「\(B\) 個以上」を目指す場合は，0成分が不足しているときに強いペナルティを与えます。

$$
F_{\mathrm{zero}}(A)
=
\begin{cases}
|B-Z(A)|^3, & Z(A)\ge B,\\
5|B-Z(A)|^3, & Z(A)<B.
\end{cases}
$$

「\(B\) 個以下」を目指す場合は，0成分が多すぎるときに強いペナルティを与えます。

$$
F_{\mathrm{zero}}(A)
=
\begin{cases}
|B-Z(A)|^3, & Z(A)\le B,\\
5|B-Z(A)|^3, & Z(A)>B.
\end{cases}
$$

0成分の個数を指定しない場合は，

$$
F_{\mathrm{zero}}(A)=0
$$

とします。

## 4. 合計評価値

最終的な評価値は，

$$
F_{\mathrm{total}}(A)
=
F_{\mathrm{abs}}(A)
+
F_{\mathrm{excess}}(A)
+
F_{\mathrm{sparse}}(A)
+
F_{\mathrm{zero}}(A)
$$

です。
""")
